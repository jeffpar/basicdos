;SINGLE PRECISION MOVE ROUTINES
;***************************************************************
$MOVFR:			;MOVE (BXDX) TO FAC
	MOV	WORD PTR $FACLO,DX
	MOV	WORD PTR $FAC-1,BX
	RET

$MOVRF:			;MOVE THE FAC INTO REGISTERS (BXDX)
	MOV	DX,WORD PTR $FACLO
	MOV	BX,WORD PTR $FAC-1
	RET
